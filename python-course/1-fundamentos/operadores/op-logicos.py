#AND
resultado = True & True #devuelve True
resultado = True & False #devuelve False
resultado = False & True #devuelve False
resultado = False & False #devuelve False

#OR
resultado = True | True #devuelve True
resultado = False | True #devuelve
resultado = True | False #devuelve True
resultado = False | False #devuelve False

#NOT
resultado = not True #devuelve False
resultado = not False #devuelve True
